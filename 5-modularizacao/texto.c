//Modularização

//A modularização consiste em dividir o programa em partes menores,
//partes independentes, cada uma com uma responsabilidade específica.
//Cada uma dessas partes menores é chamada de subrotina ou função.
//Criar subrotinas evita repetições, facilita a manutenção do código,
//torna o código mais organizado e legível,
//facilita o reaproveitamento de código em outros programas (através de bibliotecas),
//e permite que várias pessoas trabalhem no mesmo projeto sem conflitos,
//pois cada um pode trabalhar em uma parte específica do código.
//Vantagens da modularização: organização, reutilização e manutenção do código.

//Funções pré-definidas nas bibliotecas padrão de C.

//Essas funções estão disponíveis em bibliotecas padrão do C.
//São também conhecidas como funções de biblioteca.

//Passagem de parâmetro para uma função por valor ou por referência.

//A passagem de parâmetro por valor significa que estamos enviando uma cópia do valor para a função.
//O valor original permanece inalterado.
//A passagem de parâmetro por referência significa que estamos enviando o endereço de memória do valor
//para a função. A função pode acessar e modificar o valor original.

//Escopo das variáveis.

//Onde e como elas podem ser acessadas dentro do programa.
//Existem o escopo local e o  escopo global.
//Variáveis declaradas dentro de uma função são variáveis locais,
//e só podem ser acessadas dentro dessa função.
//Variáveis declaradas fora de qualquer função são variáveis globais,
//e podem ser acessadas por toda parte no programa.


//Funções e procedimentos

//Funções são blocos de código que realizam uma tarefa específica e retornam um valor.
//Procedimentos são blocos de código que realizam uma tarefa específica, mas não retornam um valor.